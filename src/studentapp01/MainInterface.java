    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sname = jTextStudentName.getText();
        double scgpa = Double.parseDouble(jTextStudentCGPA.getText());
        String sgrade = jCBStudentGrade.getSelectedItem().toString();
        studentlist.add(new Student(sname,sgrade,scgpa));
        dtm.setRowCount(0);
        for (int i = 0; i < studentlist.size(); i++) {
            Object[] objs = {studentlist.get(i).studetname, studentlist.get(i).studentgrade, studentlist.get(i).studentcgpa};
            dtm.addRow(objs);
        }
        
        clearField();
    }//GEN-LAST:event_jButton1ActionPerformed

