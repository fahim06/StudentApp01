    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Delete this data", "Delete", dialogButton);
        if(dialogResult == 0) {
            dtm.removeRow(row);
            studentlist.remove(row);
            dtm.setRowCount(0);
            for (int i = 0; i < studentlist.size(); i++) {
                Object[] objs = {studentlist.get(i).studetname,studentlist.get(i).studentgrade,studentlist.get(i).studentcgpa};
                dtm.addRow(objs);
            }
            clearField();
        } else {

        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String updatedstudentname = jTextStudentName.getText();
        double updatedcgpa = Double.parseDouble(jTextStudentCGPA.getText());
        String updatedgrade = jCBStudentGrade.getSelectedItem().toString();
        studentlist.get(row).studetname = updatedstudentname;
        studentlist.get(row).studentcgpa = updatedcgpa;
        studentlist.get(row).studentgrade = updatedgrade;
        dtm.setRowCount(0);
        for (int i = 0; i < studentlist.size(); i++) {
            Object[] objs = {studentlist.get(i).studetname,studentlist.get(i).studentgrade,studentlist.get(i).studentcgpa};
            dtm.addRow(objs);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this,"Search Student Name:");
        for (int i = 0; i < studentlist.size(); i++) {
            if (studentlist.get(i).studetname.equalsIgnoreCase(input)){
                JOptionPane.showMessageDialog(jButton4, "Found!!!","Search Student Name",2);
                jTextStudentName.setText(studentlist.get(i).studetname);
                jTextStudentCGPA.setText(String.valueOf(studentlist.get(i).studentcgpa));
                String grade = studentlist.get(i).studentgrade;
                for (int j = 0; j<(jCBStudentGrade.getItemCount());j++){
                    if (jCBStudentGrade.getItemAt(i).equalsIgnoreCase(grade)){
                        jCBStudentGrade.setSelectedIndex(i);
                    }
                }
                return;
            }
        }
        JOptionPane.showMessageDialog(jButton4, "Not Found!!!","Search Student Name",2);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        row = jTable2.getSelectedRow();
        col = jTable2.getColumnCount();
        System.out.println(row+","+col);
        jTextStudentName.setText(dtm.getValueAt(row, 0).toString());
        jTextStudentCGPA.setText(dtm.getValueAt(row, 1).toString());
        String location = dtm.getValueAt(row, 2).toString();
        for (int i = 0; i<(jCBStudentGrade.getItemCount());i++){
            if (jCBStudentGrade.getItemAt(i).equalsIgnoreCase(location)){
                jCBStudentGrade.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_jTable2MouseClicked

